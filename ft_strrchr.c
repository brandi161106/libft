char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (c == s[i])
			j = i;
		i++;
	}
	if (j != '0')
		return (&s[j]);
	return (NULL);
}
